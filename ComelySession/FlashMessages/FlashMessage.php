declare(strict_types=1);

namespace Comely\IO\Session\ComelySession\FlashMessages;

/**
 * Class FlashMessage
 * @package Comely\IO\Session\ComelySession\FlashMessages
 */
class FlashMessage
{
    /** @var string */
    private $type;
    /** @var string */
    private $message;

    /**
     * FlashMessage constructor.
     * @param string $type
     * @param string $message
     */
    public function __construct(string $type, string $message)
    {
        $this->type = $type;
        $this->message = $message;
    }

    /**
     * @return string
     */
    public function type(): string
    {
        return $this->type;
    }

    /**
     * @return string
     */
    public function message(): string
    {
        return $this->message;
    }
}